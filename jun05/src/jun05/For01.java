package jun05;

import java.util.Iterator;

// for문
/*
프로그래밍에서 필요에 따라 특정 명령을 반복적으로 실행해야 하 때가 있습니다
이때 사용되는 구문이 반복문입니다.
자바에는 3개의 반복문이 있습니다.
for, while, do~while
문법 
for(변수 초기화; 변수 조건문; 변수증감식) {
	변수 조건문이 참일때 실행하는 반복문;
 */

public class For01 {
	public static void main(String[] args) {
		for (int i  = 10; i>=0; i--) {
			System.out.println(i);
			System.out.println("발사");
		}
		

		
		
		
		
	}
}
